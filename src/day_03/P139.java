package day_03;

public class P139 {
	public static void main(String[] args)  //void 반환값이 없다 .
	{
		
		int input = -90;
		
	
		//삼항연산자 사용가능  if문으로 사용방법
		
	if (input>0)
	{
		
		System.out.println("양수");	
		
	}else if(input<0)
	{
		System.out.println("음수");
		
	}else if(input==0)
	{
		
		System.out.println(0);
		
	}

	System.out.println("========main end =============");
	
	// 메인 함수가 종료되기 직전에 생략되어있는 문장 ==return ;
	
	
	
	
	
	
	}
	

}

